@app.route('/add_case', methods=['POST'])
def add_case():
    data = request.json
    
    filing_date = datetime.strptime(data['filing_date'], "%Y-%m-%d")
    age = calculate_case_age(filing_date)

    case = {
        "case_id": len(cases)+1,
        "urgency": data['urgency'],
        "adjournments": data['adjournments'],
        "age": age
    }

    priority = calculate_priority(case)

    features = np.array([[age, data['urgency'], data['adjournments']]])
    delay_risk = int(model.predict(features)[0])

    hearing_date = judges[1]["next_available"] + timedelta(days=7)
    judges[1]["next_available"] = hearing_date

    case.update({
        "priority": priority,
        "delay_risk": delay_risk,
        "hearing_date": hearing_date.strftime("%Y-%m-%d")
    })

    cases.append(case)

    return jsonify(case)

@app.route('/cases', methods=['GET'])
def get_cases():
    return jsonify(cases)

