def calculate_case_age(filing_date):
    return (datetime.today() - filing_date).days

def calculate_priority(case):
    score = (
        0.4 * case['urgency'] +
        0.3 * (case['age']/30) +
        0.2 * case['adjournments']
    )
    return round(score,2)

